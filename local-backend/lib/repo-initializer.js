import fs from 'fs/promises';
import path from 'path';
import yaml from 'js-yaml';

export class RepoInitializer {
  constructor() {
    this.version = '1.0.0';
  }

  /**
   * Initialize .pocketdev/ structure in a target repository
   * @param {string} repoPath - Path to the repository
   * @param {object} config - Repository configuration
   */
  async initializeRepo(repoPath, config) {
    const pocketdevPath = path.join(repoPath, '.pocketdev');
    
    // Check if already initialized
    try {
      await fs.access(pocketdevPath);
      console.log('.pocketdev/ already exists, updating config...');
    } catch {
      // Create directory structure
      await this.createDirectoryStructure(repoPath);
    }
    
    // Create or update config
    await this.createConfig(pocketdevPath, config);
    
    // Create initial memory files
    await this.createMemoryFiles(pocketdevPath);
    
    // Create README
    await this.createReadme(pocketdevPath);
    
    return {
      success: true,
      path: pocketdevPath,
      message: 'Repository initialized with PocketDev memory system'
    };
  }

  async createDirectoryStructure(repoPath) {
    const dirs = [
      '.pocketdev',
      '.pocketdev/engineers',
      '.pocketdev/engineers/frontend',
      '.pocketdev/engineers/backend',
      '.pocketdev/engineers/devops',
      '.pocketdev/engineers/fullstack',
      '.pocketdev/engineers/qa_manual',
      '.pocketdev/project',
      '.pocketdev/workspaces'
    ];
    
    for (const dir of dirs) {
      await fs.mkdir(path.join(repoPath, dir), { recursive: true });
    }
  }

  async createConfig(pocketdevPath, config) {
    const configData = {
      version: this.version,
      project: {
        name: config.name || path.basename(config.repository),
        repository: config.repository,
        default_branch: config.defaultBranch || 'main',
        initialized: new Date().toISOString(),
        last_updated: new Date().toISOString()
      },
      memory_settings: {
        enabled: true,
        max_memories_per_type: 50,
        auto_cleanup_days: 90,
        performance_threshold_ms: 1000,
        failure_detection: true,
        pattern_recognition: true
      },
      engineers: {
        frontend: { memory_enabled: true },
        backend: { memory_enabled: true },
        devops: { memory_enabled: true },
        fullstack: { memory_enabled: true },
        qa_manual: { memory_enabled: true }
      }
    };
    
    await fs.writeFile(
      path.join(pocketdevPath, 'config.json'),
      JSON.stringify(configData, null, 2)
    );
  }

  async createMemoryFiles(pocketdevPath) {
    const memoryTypes = ['performance.yml', 'failures.yml', 'patterns.yml'];
    const engineers = ['frontend', 'backend', 'devops', 'fullstack', 'qa_manual'];
    
    for (const engineer of engineers) {
      for (const memoryType of memoryTypes) {
        const content = `# ${engineer.charAt(0).toUpperCase() + engineer.slice(1)} ${memoryType.replace('.yml', '').charAt(0).toUpperCase() + memoryType.replace('.yml', '').slice(1)}
# Auto-generated by PocketDev

memories: []
`;
        
        await fs.writeFile(
          path.join(pocketdevPath, 'engineers', engineer, memoryType),
          content
        );
      }
    }
  }

  async createReadme(pocketdevPath) {
    const readme = `# PocketDev Team Memory

This directory contains the collective memory of your AI development team. It stores learnings, optimizations, and patterns discovered during task execution.

## How It Works

1. **Before Task**: Relevant memories are loaded into the engineer's context
2. **During Task**: Engineer applies learned optimizations and avoids known failures  
3. **After Task**: New learnings are extracted and stored
4. **Over Time**: Engineers become more efficient and consistent

## Memory Types

- **performance.yml**: Performance optimizations discovered
- **failures.yml**: Failed approaches to avoid
- **patterns.yml**: Successful patterns to reuse

## Relationship to CLAUDE.md

- **CLAUDE.md**: Static project configuration (conventions, commands, overview)
- **.pocketdev/**: Dynamic task-specific learnings that grow over time

The .pocketdev/ memories augment CLAUDE.md by capturing what changes and improves through experience.

## Best Practices

1. Commit this directory to version control
2. Review memories periodically to ensure quality
3. Share significant learnings with your team
4. Clean up outdated memories after major refactors

Generated by PocketDev v${this.version}
`;
    
    await fs.writeFile(
      path.join(pocketdevPath, 'README.md'),
      readme
    );
  }
}

export default RepoInitializer;